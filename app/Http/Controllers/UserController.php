    {

        

        $request->validate([
            'name' => 'required',
            'email' => 'required',
            'password' => 'required',
        ]);

        $request->merge([
            'type' => "Free",
            'status' => 1,
            'rol_id' => 1,
            'password' => bcrypt($request->get('password'))
            
        ]);

        $user = User::create($request->all());
